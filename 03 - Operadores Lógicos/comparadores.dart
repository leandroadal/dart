void main() {
  // Comparadores

  // == - igual
  // !- - diferente
  // > - maior que
  // < - menor que
  // >= maior ou igual a
  // <= menor ou igual a

  int n = 2;
  int n2 = 2;
  bool r = n > n2;
  print(r);
  r = n < n2;
  print(r);
  r = n <= n2;
  print(r);
  r = n >= n2;
  print(r);
  r = n == n2;
  print(r);
  r = n != n2;
  print(r);
}
